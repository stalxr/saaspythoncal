def run():
    print("\n=== Площадь прямоугольного треугольника ===")
    try:
        катет_a = float(input("Введите первый катет: "))
        катет_b = float(input("Введите второй катет: "))
        
        if катет_a <= 0 or катет_b <= 0:
            print("Ошибка: катеты должны быть положительными числами!")
            return
        
        площадь = 0.5 * катет_a * катет_b
        print(f"\nПлощадь треугольника: {площадь}")
    except ValueError:
        print("Ошибка: введите корректное число!")

