def run():
    print("\n=== Список задач ===")
    задачи = []
    
    while True:
        print("\n1. Показать задачи")
        print("2. Добавить задачу")
        print("3. Удалить задачу")
        print("4. Выход")
        
        выбор = input("Выберите действие: ").strip()
        
        if выбор == "1":
            if задачи:
                print("\nЗадачи:")
                for i, задача in enumerate(задачи, 1):
                    print(f"{i}. {задача}")
            else:
                print("\nЗадачи: пусто")
                
        elif выбор == "2":
            задача = input("Введите задачу: ").strip()
            if задача:
                задачи.append(задача)
                print("Задача добавлена!")
            else:
                print("Задача не может быть пустой!")
                
        elif выбор == "3":
            if задачи:
                print("\nТекущие задачи:")
                for i, задача in enumerate(задачи, 1):
                    print(f"{i}. {задача}")
                try:
                    номер = int(input("Введите номер задачи для удаления: "))
                    if 1 <= номер <= len(задачи):
                        удалённая = задачи.pop(номер - 1)
                        print(f"Удалена задача: {удалённая}")
                    else:
                        print("Неверный номер задачи!")
                except ValueError:
                    print("Введите число!")
            else:
                print("Список задач пуст!")
                
        elif выбор == "4":
            print("До свидания!")
            break
        else:
            print("Неверный выбор!")

