def run():
    print("\n=== Калькулятор ===")
    try:
        первое = float(input("Введите первое число: "))
        второе = float(input("Введите второе число: "))
        оператор = input("Введите оператор (+, -, *, /): ").strip()
        
        if оператор == "+":
            результат = первое + второе
        elif оператор == "-":
            результат = первое - второе
        elif оператор == "*":
            результат = первое * второе
        elif оператор == "/":
            if второе == 0:
                print("Ошибка: деление на ноль!")
                return
            результат = первое / второе
        else:
            print("Неизвестный оператор!")
            return
        
        print(f"\nРезультат: {результат}")
    except ValueError:
        print("Ошибка: введите корректное число!")

