def run():
    print("\n=== Калькулятор бюджета ===")
    доходы = []
    расходы = []
    
    while True:
        print("\n1. Добавить доход")
        print("2. Добавить расход")
        print("3. Показать баланс")
        print("4. Выход")
        
        выбор = input("Выберите действие: ").strip().lower()
        
        if выбор == "1":
            try:
                сумма = int(input("Введите сумму дохода: "))
                if сумма > 0:
                    доходы.append(сумма)
                    print("Доход добавлен!")
                else:
                    print("Сумма должна быть положительной!")
            except ValueError:
                print("Введите корректное число!")
                
        elif выбор == "2":
            try:
                сумма = int(input("Введите сумму расхода: "))
                if сумма > 0:
                    расходы.append(сумма)
                    print("Расход добавлен!")
                else:
                    print("Сумма должна быть положительной!")
            except ValueError:
                print("Введите корректное число!")
                
        elif выбор == "3":
            всего_доходов = sum(доходы)
            всего_расходов = sum(расходы)
            баланс = всего_доходов - всего_расходов
            
            print(f"\nВсего доходов: {всего_доходов}")
            print(f"Всего расходов: {всего_расходов}")
            print(f"Баланс: {баланс}")
            
            if баланс > 0:
                print("У вас положительный баланс!")
            elif баланс < 0:
                print("Внимание! У вас дефицит бюджета!")
            else:
                print("Бюджет сбалансирован")
                
        elif выбор == "4" or выбор == "выход":
            print("До свидания!")
            break
        else:
            print("Неверный выбор!")

